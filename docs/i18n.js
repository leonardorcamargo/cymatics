let currentLang;

function applyLang(lang) {
  const t = translations[lang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';
  document.getElementById('lang-toggle').textContent = lang === 'pt' ? 'EN' : 'PT';
  localStorage.setItem('cymatics-lang', lang);
  currentLang = lang;
}

function initLang() {
  const saved    = localStorage.getItem('cymatics-lang');
  const browser  = navigator.language || navigator.userLanguage || '';
  const detected = browser.toLowerCase().startsWith('pt') ? 'pt' : 'en';

  applyLang(saved || detected);

  document.getElementById('lang-toggle').addEventListener('click', () => {
    applyLang(currentLang === 'pt' ? 'en' : 'pt');
  });
}

initLang();
